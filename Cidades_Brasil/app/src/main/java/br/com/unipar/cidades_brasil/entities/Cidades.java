package br.com.unipar.cidades_brasil.entities;

public class Cidades {
    private Integer estadosId;
    private Integer id;
    private String nome;
    private String url;
    private String municipiosUrl;

    public Integer getEstadosIdId() {
        return estadosId;
    }

    public void setEstadosId(Integer estadosIdId) {
        this.estadosId = estadosId;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String title) {
        this.nome = nome;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getMunicipiosUrlUrl() {
        return municipiosUrl;
    }

    public void setMunicipiosUrlUrl(String municipiosUrlUrl) {
        this.municipiosUrl = municipiosUrl;
    }
}